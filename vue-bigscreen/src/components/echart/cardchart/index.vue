    methods: {
        async fetchData(path) {
            const datePattern = /^\d{4}-(0[1-9]|1[0-2])$/;
            const filterData = (datas) => {
                let filteredDatas = {};
                for (const key in datas) {
                    if (datePattern.test(key)) {
                        filteredDatas[key] = datas[key];
                    }
                }
                return filteredDatas;
            }
            let starResponse = await axios.get(path + '/stars.json');
            let starData = await starResponse.data;
            starData = filterData(starData);
            let forkResponse = await axios.get(path + '/technical_fork.json');
            let forkData = await forkResponse.data;
            forkData = filterData(forkData);
            let issueResponse = await axios.get(path + '/issues_new.json');
            let issueData = await issueResponse.data;
            issueData = filterData(issueData);
            let requestResponse = await axios.get(path + '/change_requests.json');
            let requestData = await requestResponse.data;
            requestData = filterData(requestData);


            // 
            let starKeys = Object.keys(starData);
            let forkKeys = Object.keys(forkData);
            let issueKeys = Object.keys(issueData);
            let requestKeys = Object.keys(requestData);

            // 
            let commonKeys = starKeys.filter(value => forkKeys.includes(value) && issueKeys.includes(value) && requestKeys.includes(value));
            let firstyear = parseInt(commonKeys[0].split('-')[0]);
            let data = {
                months: ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"],
                year: [...new Set(commonKeys.map(key => key.split('-')[0]))],
                stardata: commonKeys.map((key) => {
                    const monthIndex = parseInt(key.split('-')[1]) - 1; //  (0-11)
                    const yearIndex = parseInt(key.split('-')[0]) - firstyear; // 
                    const value = starData[key];
                    return [yearIndex, monthIndex, value]; //  [, , ] 
                }),
                forkdata: commonKeys.map((key) => {
                    const monthIndex = parseInt(key.split('-')[1]) - 1;
                    const yearIndex = parseInt(key.split('-')[0]) - firstyear;
                    const value = forkData[key];
                    return [yearIndex, monthIndex, value];
                }),
                issuedata: commonKeys.map((key) => {
                    const monthIndex = parseInt(key.split('-')[1]) - 1;
                    const yearIndex = parseInt(key.split('-')[0]) - firstyear;
                    const value = issueData[key];
                    return [yearIndex, monthIndex, value];
                }),
                requestdata: commonKeys.map((key) => {
                    const monthIndex = parseInt(key.split('-')[1]) - 1;
                    const yearIndex = parseInt(key.split('-')[0]) - firstyear;
                    const value = requestData[key];
                    return [yearIndex, monthIndex, value];
                }),

            }

            return data
        }
